package co.edu.uniquindio.storify.dto;

public class TableMusicaDto {
	public String nombreArtista;
	public String codigoCancion;
	public String nombreCancion;
	/**
	 * @return the nombreArtista
	 */
	public String getNombreArtista() {
		return nombreArtista;
	}
	/**
	 * @param nombreArtista the nombreArtista to set
	 */
	public void setNombreArtista(String nombreArtista) {
		this.nombreArtista = nombreArtista;
	}
	/**
	 * @return the codigoCancion
	 */
	public String getCodigoCancion() {
		return codigoCancion;
	}
	/**
	 * @param codigoCancion the codigoCancion to set
	 */
	public void setCodigoCancion(String codigoCancion) {
		this.codigoCancion = codigoCancion;
	}
	/**
	 * @return the nombreCancion
	 */
	public String getNombreCancion() {
		return nombreCancion;
	}
	/**
	 * @param nombreCancion the nombreCancion to set
	 */
	public void setNombreCancion(String nombreCancion) {
		this.nombreCancion = nombreCancion;
	}
	
	
}
